<script>
$(document).ready(function() {

    load_data();

    function load_data() {
        $.ajax({
            url: "<?php echo base_url(); ?>index.php/welcome/fetch",
            method: "POST",
            success: function(data) {
                $('#business_list').html(data);
            }
        })
    }

    $(document).on('mouseenter', '.rating', function() {
        var index = $(this).data('index');
        var paket_id = $(this).data('paket_id');
        remove_background(paket_id);
        for (var count = 1; count <= index; count++) {
            $('#' + paket_id + '-' + count).css('color', '#ffcc00');
        }
    });

    function remove_background(paket_id) {
        for (var count = 1; count <= 5; count++) {
            $('#' + paket_id + '-' + count).css('color', '#ccc');
        }
    }

    $(document).on('click', '.rating', function() {
        var index = $(this).data('index');
        var paket_id = $(this).data('paket_id');
        $.ajax({
            url: "<?php echo base_url(); ?>index.php/welcome/insert",
            method: "POST",
            data: {
                index: index,
                paket_id: paket_id
            },
            success: function(data) {
                load_data();
                alert("You have rate " + index + " out of 5");
            }
        })
    });

    $(document).on('mouseleave', '.rating', function() {
        var index = $(this).data('index');
        var paket_id = $(this).data('paket_id');
        var rating = $(this).data('rating');
        remove_background(paket_id);
        for (var count = 1; count <= rating; count++) {
            $('#' + paket_id + '-' + count).css('color', '#ffcc00');
        }
    });

});
</script>